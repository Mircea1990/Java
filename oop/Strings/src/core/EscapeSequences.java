package core;

public class EscapeSequences {

	public static void main(String[] args) {
		System.out.println("===== Escape sequences =====");
		System.out.println();

		System.out.println("My favourite car is \"Honda\" because it looks good");
		System.out.println("Path to the source code: D:\\Java\\source");

		System.out.println("some text line 1\nsome text line 2");

		System.out.println("Unicode characters, \u00A9");
	}

}
