package jakartaee.examples.jsonb.nullvalues;

/**
 * A null values object.
 */
public class NullValues {

    /**
     * Stores the string.
     */
    private String string;

    /**
     * Get the string.
     *
     * @return the string.
     */
    public String getString() {
        return string;
    }

    /**
     * Set the string.
     *
     * @param string the string.
     */
    public void setString(String string) {
        this.string = string;
    }
}
